package Fariba.Learn.net;

public class Operators {
    public static void main(String[] args) {
        int speed = 30;
        int speedLimited = 45;

        System.out.println("speed is " + speed);
        System.out.println("speedLimited is " + speedLimited);

        boolean result = speed > speedLimited;
        System.out.println("speed > speedLimited is ");
        System.out.println(result);

        boolean result1 = speed < speedLimited;
        System.out.println("speed > speedLimited is ");
        System.out.println(result1);


    }
}

